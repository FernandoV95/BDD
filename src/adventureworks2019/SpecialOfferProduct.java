    private void TSOPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TSOPMouseClicked
        // TODO add your handling code here:
        try {
            int selec = TSOP.rowAtPoint(evt.getPoint());
            int fila = TSOP.getSelectedRow();
            int ID = Integer.parseInt(TSOP.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conector.getConexion();

            String consulta = "select ProductID, rowguid, ModifiedDate from sales.SpecialOfferProduct  WHERE SpecialOfferID = ?";
            ps = con.prepareStatement(consulta);
            ps.setInt(1, ID);
            rs = ps.executeQuery();

            while (rs.next()) {
                Id.setText(TSOP.getValueAt(selec, 0) + "");
                P.setText(TSOP.getValueAt(selec, 1) + "");
                r.setText(TSOP.getValueAt(selec, 2) + "");
                Mo.setText(TSOP.getValueAt(selec, 3) + "");

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error X_x");

        }


    }//GEN-LAST:event_TSOPMouseClicked

