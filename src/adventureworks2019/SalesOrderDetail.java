    public void ver() {
        DefaultTableModel T = (DefaultTableModel) TSOD.getModel();
        T.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columna;

        int[] anchos = {90, 120, 100, 60, 70, 100, 90, 110, 80, 270, 150};
        for (int i = 0; i < TSOD.getColumnCount(); i++) {
            TSOD.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }

        try {
            Connection con = Conector.getConexion();
            String consulta = "select * from Sales.SalesOrderDetail";
            ps = con.prepareStatement(consulta);
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columna = rsmd.getColumnCount();

            while (rs.next()) {
                Object[] fila = new Object[columna];
                for (int i = 0; i < columna; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                T.addRow(fila);
            }
        } catch (SQLException e) {
            System.out.println(e.toString());
        }
    }

