    private void TSODMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TSODMouseClicked
        // TODO add your handling code here:

        try {
            int selec = TSOD.rowAtPoint(evt.getPoint());
            int fila = TSOD.getSelectedRow();
            int ID = Integer.parseInt(TSOD.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conector.getConexion();

            String consulta = "select SalesOrderDetailID,CarrierTrackingNumber,OrderQty, \n"
                    + "ProductID, SpecialOfferID,UnitPrice, UnitPriceDiscount, LineTotal,"
                    + "rowguid, ModifiedDate from Sales.SalesOrderDetail  "
                    + "WHERE SalesOrderID = ?";
            ps = con.prepareStatement(consulta);
            ps.setInt(1, ID);
            rs = ps.executeQuery();

            while (rs.next()) {
                SOID.setText(TSOD.getValueAt(selec, 0) + "");
                SODID.setText(TSOD.getValueAt(selec, 1) + "");
                CTN.setText(TSOD.getValueAt(selec, 2) + "");
                OQ.setText(TSOD.getValueAt(selec, 3) + "");
                PID.setText(TSOD.getValueAt(selec, 4) + "");
                SOfID.setText(TSOD.getValueAt(selec, 5) + "");
                UP.setText(TSOD.getValueAt(selec, 6) + "");
                UPD.setText(TSOD.getValueAt(selec, 7) + "");
                LT.setText(TSOD.getValueAt(selec, 8) + "");
                R.setText(TSOD.getValueAt(selec, 9) + "");
                MD.setText(TSOD.getValueAt(selec, 10) + "");

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error X_x");

        }
    }//GEN-LAST:event_TSODMouseClicked

